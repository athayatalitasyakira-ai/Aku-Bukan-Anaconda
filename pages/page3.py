# Baca data
df = pd.read_csv(StringIO(data))

# Konversi kolom Tanggal (campuran string dan serial Excel)
def convert_date(val):
    try:
        # Coba sebagai angka (serial Excel)
        serial = float(val)
        return pd.to_datetime(serial, unit='d', origin='1899-12-30')
    except ValueError:
        # Jika bukan angka, parse sebagai string tanggal
        return pd.to_datetime(val, format='%m/%d/%Y')

df['Tanggal'] = df['Tanggal'].apply(convert_date)

# Sortir berdasarkan tanggal dan reset index
df = df.sort_values('Tanggal').reset_index(drop=True)

# Opsi indeks yang tersedia
saham_options = ['Composite_Index', 'LQ45', 'IDX30']

# Fungsi untuk plot grafik
def plot_saham(saham):
    plt.figure(figsize=(14, 7))
    plt.plot(df['Tanggal'], df[saham], marker='o', linestyle='-', color='b')
    plt.title(f'Data Historis: {saham}')
    plt.xlabel('Tanggal')
    plt.ylabel('Nilai Indeks')
    plt.grid(True)
    plt.xticks(rotation=45)
    plt.tight_layout()
    plt.show()
    
    # Penjelasan
    print(f"\nPenjelasan untuk {saham}:")
    print("Grafik menampilkan pergerakan nilai indeks sepanjang waktu.")
    print(f"Periode data: {df['Tanggal'].min().strftime('%d %B %Y')} hingga {df['Tanggal'].max().strftime('%d %B %Y')}")
    print(f"Nilai terendah: {df[saham].min():.2f}")
    print(f"Nilai tertinggi: {df[saham].max():.2f}")
    print(f"Rata-rata: {df[saham].mean():.2f}")
    print("Amati tren naik/turun untuk analisis lebih lanjut.")

# Dropdown interaktif
interact(plot_saham, saham=Dropdown(options=saham_options, value='Composite_Index', description='Pilih Indeks:'));
